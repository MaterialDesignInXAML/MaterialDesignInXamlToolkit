namespace MaterialDesignThemes.Wpf;

/// <summary>
/// Enumeration of options for how a floating hint should align with respect to a prefix/suffix
/// </summary>
public enum PrefixSuffixHintBehavior
{
    /// <summary>
    /// Aligns the hint with the left/right edge of the prefix/suffix respectively
    /// </summary>
    AlignWithPrefixSuffix,
    /// <summary>
    /// Aligns the hint with the left/right edge of the text
    /// </summary>
    AlignWithText
}
