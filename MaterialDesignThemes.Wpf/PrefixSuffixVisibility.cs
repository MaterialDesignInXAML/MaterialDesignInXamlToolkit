namespace MaterialDesignThemes.Wpf;

/// <summary>
/// Enumeration of options for when a prefix/suffix should be visible
/// </summary>
public enum PrefixSuffixVisibility
{
    /// <summary>
    /// Show prefix/suffix only when the text box is focused or contains user-entered text
    /// </summary>
    VisibleWhenFocusedOrNonEmpty,
    /// <summary>
    /// Always show prefix/suffix
    /// </summary>
    AlwaysVisible
}
